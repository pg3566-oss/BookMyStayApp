/**
 * UC1 - Application Entry Point for Book My Stay App
 *
 * This class demonstrates the starting point of a Java application.
 * It prints a welcome message, application name, and version details.
 *
 * @author Pallavi
 * @version 1.0
 */
public class BookMyStayApp {

    /**
     * Main method - Entry point of the application
     * JVM starts execution from here.
     *
     * @param args Command line arguments
     */
    public static void main(String[] args) {

        // Welcome Message
        System.out.println("======================================");
        System.out.println("   Welcome to Book My Stay App 🏨");
        System.out.println("======================================");

        // Application Details
        System.out.println("Application Name : Hotel Booking Management System");
        System.out.println("Version          : v1.0");

        // Closing Message
        System.out.println("--------------------------------------");
        System.out.println("Application started successfully!");
        System.out.println("--------------------------------------");
    }
}